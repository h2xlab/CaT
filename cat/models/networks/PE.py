class PositionEmbeddingLearned(nn.Module):
    """
    Absolute pos embedding, learned.
    """
    def __init__(self, h=50, w=50, num_pos_feats=256):
        super().__init__()
        self.col_embed = nn.Embedding(h, num_pos_feats)
        self.row_embed = nn.Embedding(w, num_pos_feats)
        self.reset_parameters()
        self.w = w
        self.h = h

    def reset_parameters(self):
        nn.init.uniform_(self.row_embed.weight)
        nn.init.uniform_(self.col_embed.weight)

    # supposed input is NCHW
    def forward(self, uv_feat):
        x = uv_feat
        h, w = x.shape[-2:]
        i = torch.arange(w, device=x.device)
        j = torch.arange(h, device=x.device)
        x_emb = self.row_embed(i)
        y_emb = self.col_embed(j)
        pos = torch.cat([
            x_emb.unsqueeze(0).repeat(h, 1, 1),
            y_emb.unsqueeze(1).repeat(1, w, 1),
        ], dim=-1).permute(2, 0, 1).unsqueeze(0).repeat(x.shape[0], 1, 1, 1)
        return pos


def build_position_encoding(h, w, hidden_dim, args):
    hidden_dim = hidden_dim // 2    # embed h-feat, w-feat accordingly
    if args.position_embedding in ('v2', 'sine'):
        raise Exception("sine embed not ready yet")
        # TODO find a better way of exposing other arguments
        position_embedding = PositionEmbeddingSine(N_steps, normalize=True)
    elif args.position_embedding in ('v3', 'learned'):
        position_embedding = PositionEmbeddingLearned(h=h, w=w, num_pos_feats=hidden_dim)
    else:
        raise ValueError(f"not supported {args.position_embedding}")

    return position_embedding